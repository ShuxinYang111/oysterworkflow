export interface PlannerOptimizationPromptPayload {
  systemPrompt: string;
  userPrompt: string;
}

export interface RunPlannerOptimizationInput<TResult> {
  skill: OpenClawSkill;
  promptSet: LoadedPromptSet;
  templateVars: Record<string, string>;
  execute: (payload: PlannerOptimizationPromptPayload) => Promise<TResult>;
}

/**
 * EN: Builds the planner-optimization prompt payload.
 * @param input current skill, promptSet, and template vars.
 * @returns system/user prompt text.
 */
export function buildPlannerOptimizationPromptPayload(input: {
  skill: OpenClawSkill;
  promptSet: LoadedPromptSet;
  templateVars: Record<string, string>;
}): PlannerOptimizationPromptPayload {
  const promptSection =
    input.promptSet.plannerOptimization ??
    DEFAULT_PLANNER_OPTIMIZATION_PROMPT_SECTION;
  const systemPrompt = renderPromptTemplate(
    promptSection.system,
    input.templateVars,
  );
  const userPreamble = renderPromptTemplate(
    promptSection.userPreamble,
    input.templateVars,
  );

  return {
    systemPrompt,
    userPrompt: [
      userPreamble,
      "Current skill JSON:",
      JSON.stringify(input.skill, null, 2),
    ].join("\n"),
  };
}

/**
 * EN: Runs the standalone planner-optimization component with an injected LLM executor.
 * @param input current skill, prompt context, and executor.
 * @returns executor result.
 */
export async function runPlannerOptimization<TResult>(
  input: RunPlannerOptimizationInput<TResult>,
): Promise<TResult> {
  const payload = buildPlannerOptimizationPromptPayload({
    skill: input.skill,
    promptSet: input.promptSet,
    templateVars: input.templateVars,
  });
  return input.execute(payload);
}
